    private void btnSignupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignupActionPerformed
        // TODO add your handling code here:
        
        if (status()){
            JOptionPane.showMessageDialog(null,"::: The User already exists :::");
        } else {
            if (txtPassword.getText().equals(txtPassword2.getText())) {

                User user = new User (
                    txtFirstname.getText(),
                    txtLastname.getText(),
                    cmbGender.getSelectedItem().toString(),
                    txtMobile.getText(),
                    cmbCity.getSelectedItem().toString(),
                    txtCompany.getText(),
                    txtPhone.getText(),
                    txtAddress.getText(),
                    txtEmail.getText(),
                    txtPassword.getText()
                );
                users.add(user); //---> users.add (user [pos])

                System.out.println(users.get(0).getEmail());
                JOptionPane.showMessageDialog(null,"::: The User has been created.  Now you will be redirect to Login form. :::");
                dispose();
            } else {
                JOptionPane.showMessageDialog(null,"::: Passwords do not match :::");
            }
        }    
    }//GEN-LAST:event_btnSignupActionPerformed

    
    public boolean status() {
        boolean x = false;
        int pos=0;
        for (int i=0; i<users.size(); i++){
            if(users.get(i).getEmail().equals(txtEmail.getText())){
                x=true;
                pos=i;
                break;
            }
        }
        return x;
    }
    
